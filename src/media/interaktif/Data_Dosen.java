    public void tampil_database(){
        Object[]baris = {"NIP", "Nama", "Username", "Password", "Confirm"};
        tabmode = new DefaultTableModel(null, baris);
        tbl_dsn.setModel(tabmode);
        try{
            hasil = dsn.tampilData();
            while(hasil.next()){
                dsn.setNip(hasil.getString("NIP"));
                dsn.setNama(hasil.getString("Nama"));
                dsn.setUsername(hasil.getString("Username"));
                dsn.setPass(hasil.getString("Password"));
                dsn.setConfirm(hasil.getString("Confirm"));
                String[] data = {dsn.getNip(), dsn.getNama(), dsn.getUsername(), dsn.getPass(), dsn.getConfirm()};
                tabmode.addRow(data);
            } 
        } catch(Exception e){
        }
    }
    

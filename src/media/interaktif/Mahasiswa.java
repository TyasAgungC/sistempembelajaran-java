    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        if(txt_nim.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, NIM belum diisi!");
            txt_nim.requestFocus();
        }else if(txt_nama.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Nama belum diisi!");
            txt_nama.requestFocus();
        }else if(txt_prodi.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Program Studi belum diisi!");
            txt_prodi.requestFocus();
        }else if(txt_ang.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Angkatan belum diisi!");
            txt_ang.requestFocus();
        }else if(txt_off.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Offering belum diisi!");
            txt_off.requestFocus();
        }else if(txt_pass.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Password belum diisi!");
            txt_pass.requestFocus();
        }else {
            String nim = "", nama = "", prodi = "", angkatan = "", off = "", pass = "";
            try {
                mhs.setNim(txt_nim.getText());
                mhs.setNama(txt_nama.getText());
                mhs.setProdi(txt_prodi.getText());
                mhs.setAng(txt_ang.getText());
                mhs.setOff(txt_off.getText());
                mhs.setPass(txt_pass.getText());
                
                mhs.ubahData(mhs.getNim(), mhs.getNama(), mhs.getProdi(), mhs.getAng(), mhs.getOff(), mhs.getPass());
                
                JOptionPane.showMessageDialog(null, "Data berhasil diubah" , "Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
                tampil_database();
                reset_text();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Data gagal diubah","Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_editActionPerformed


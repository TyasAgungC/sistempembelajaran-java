    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        if(txt_nama.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Nama belum diisi!");
            txt_nama.requestFocus();
        }else if(txt_user.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Username belum diisi!");
            txt_user.requestFocus();
        }else if(txt_pass.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Password belum diisi!");
            txt_pass.requestFocus();
        }else if(txt_confirm.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Password belum diisi!");
            txt_confirm.requestFocus();
            
        }else {
            String nama = "", username = "",  pass = "", confirm = "";
            try {
                akun.setnama(txt_nama.getText());
                akun.setusername(txt_user.getText());
                akun.setPassword(txt_pass.getText());
                akun.setconfirm(txt_confirm.getText());
                
                akun.ubahData(akun.getnama(), akun.getusername(), akun.getPassword(), akun.getconfirm());
                
                JOptionPane.showMessageDialog(null, "Data berhasil diubah" , "Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
                tampil_database();
                reset_text();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Data gagal diubah","Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        }                                     

    }//GEN-LAST:event_btn_ubahActionPerformed
   
    

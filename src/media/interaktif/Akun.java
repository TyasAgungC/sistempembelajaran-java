    public void tampil_database(){
        Object[]baris = {"nama", "username", "password", "confirm"};
        tabmode = new DefaultTableModel(null, baris);
        tbl_akun.setModel(tabmode);
        try{
            hasil = akun.tampilData();
            while(hasil.next()){
                akun.setnama(hasil.getString("nama"));
                akun.setusername(hasil.getString("username"));
                akun.setPassword(hasil.getString("password"));
                akun.setconfirm(hasil.getString("confirm"));
                String[] data = {akun.getnama(), akun.getusername(), akun.getPassword(), akun.getconfirm()};
                tabmode.addRow(data);
            } 
        } catch(Exception e){
        }
    }
    

    public void tampil_database(){
        Object[]baris = {"nim", "nama", "prodi", "angkatan", "off", "pass"};
        tabmode = new DefaultTableModel(null, baris);
        tbl_mhs.setModel(tabmode);
        try{
            hasil = mhs.tampilData();
            while(hasil.next()){
                mhs.setNim(hasil.getString("nim"));
                mhs.setNama(hasil.getString("nama"));
                mhs.setProdi(hasil.getString("prodi"));
                mhs.setAng(hasil.getString("angkatan"));
                mhs.setOff(hasil.getString("off"));
                mhs.setPass(hasil.getString("pass"));
                String[] data = {mhs.getNim(), mhs.getNama(), mhs.getProdi(), mhs.getAng(), mhs.getOff(), mhs.getPass()};
                tabmode.addRow(data);
            } 
        } catch(Exception e){
        }
    }
    

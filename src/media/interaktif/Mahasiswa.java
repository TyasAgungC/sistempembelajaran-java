    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        if(txt_nim.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, NIM belum diisi!");
            txt_nim.requestFocus();
        } else {
            if(JOptionPane.showConfirmDialog(null, "Apakah Anda yakin akan menghapus data ini ?",
                    "Warning",2)==JOptionPane.YES_OPTION){
                String nim = "";
                try {
                    mhs.setNim(txt_nim.getText());
                    mhs.hapusData(mhs.getNim());
                    
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus" , "Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
                    tampil_database();
                    reset_text();
                } catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Data gagal dihapus","Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
                }
            }
            
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void tbl_mhsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_mhsMouseClicked
        try {
            int row = tbl_mhs.rowAtPoint(evt.getPoint());
            
            String nim = tbl_mhs.getValueAt(row, 0).toString();
            String nama = tbl_mhs.getValueAt(row, 1).toString();
            String prodi = tbl_mhs.getValueAt(row, 2).toString();
            String angkatan = tbl_mhs.getValueAt(row, 3).toString();
            String off = tbl_mhs.getValueAt(row, 4).toString();
            String pass = tbl_mhs.getValueAt(row, 5).toString();
            
            txt_nim.setText(String.valueOf(nim));
            txt_nama.setText(String.valueOf(nama));
            txt_prodi.setText(String.valueOf(prodi));
            txt_ang.setText(String.valueOf(angkatan));
            txt_off.setText(String.valueOf(off));
            txt_pass.setText(String.valueOf(pass));
        } catch(Exception e){
            
        }
    }//GEN-LAST:event_tbl_mhsMouseClicked


    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        if(txt_nama.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Nama belum diisi!");
            txt_nama.requestFocus();
        } else {
            if(JOptionPane.showConfirmDialog(null, "Apakah Anda yakin akan menghapus data ini ?",
                    "Warning",2)==JOptionPane.YES_OPTION){
                String nama = "";
                try {
                    akun.setnama(txt_nama.getText());
                    akun.hapusData(akun.getnama());
                    
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus" , "Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
                    tampil_database();
                    reset_text();
                } catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Data gagal dihapus","Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
                }
            }
            
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void tbl_akunMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_akunMouseClicked
        try {
            int row = tbl_akun.rowAtPoint(evt.getPoint());
            
            String nama = tbl_akun.getValueAt(row, 0).toString();
            String username = tbl_akun.getValueAt(row, 1).toString();
            String password = tbl_akun.getValueAt(row, 2).toString();
            String confirm = tbl_akun.getValueAt(row, 3).toString();
            
            txt_nama.setText(String.valueOf(nama));
            txt_user.setText(String.valueOf(username));
            txt_pass.setText(String.valueOf(password));
            txt_confirm.setText(String.valueOf(confirm));
        } catch(Exception e){
            
        }
    }//GEN-LAST:event_tbl_akunMouseClicked


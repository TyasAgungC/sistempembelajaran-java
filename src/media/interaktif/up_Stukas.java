    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        if(txt_bab.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Kasus belum diisi!");
            txt_bab.requestFocus();
        } else {
            if(JOptionPane.showConfirmDialog(null, "Apakah Anda yakin akan menghapus data ini ?",
                    "Warning",2)==JOptionPane.YES_OPTION){
                String Bab = "";
                try {
                    stukas.setKasus(txt_bab.getText());
                    
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus" , "Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
                    tampil_database();
                    reset_text();
                } catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Data gagal dihapus","Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
                }
            }
            
        }
    }//GEN-LAST:event_btn_hapusActionPerformed
   
    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        if(txt_bab.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Bab belum diisi!");
            txt_bab.requestFocus();
        }else if(txt_kas.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Kasus belum diisi!");
            txt_kas.requestFocus();
        }else {
            String Bab = "", Kasus = "";
            try {
                stukas.setBab(txt_bab.getText());
                stukas.setKasus(txt_kas.getText());
                
                stukas.simpanData(stukas.getBab(), stukas.getKasus());
                
                JOptionPane.showMessageDialog(null, "Data berhasil tersimpan" , "Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
                tampil_database();
                reset_text();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Data gagal tersimpan","Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        }                                      

    }//GEN-LAST:event_btn_simpanActionPerformed


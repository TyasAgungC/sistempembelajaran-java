    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        if(txt_nip.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, NIP belum diisi!");
            txt_nip.requestFocus();
        } else {
            if(JOptionPane.showConfirmDialog(null, "Apakah Anda yakin akan menghapus data ini ?",
                "Warning",2)==JOptionPane.YES_OPTION){
            String NIP = "";
            try {
                dsn.setNip(txt_nip.getText());
                dsn.hapusData(dsn.getNip());

                JOptionPane.showMessageDialog(null, "Data berhasil dihapus" , "Informasi",
                    JOptionPane.INFORMATION_MESSAGE);
                tampil_database();
                reset_text();
            } catch(Exception e){
                JOptionPane.showMessageDialog(null, "Data gagal dihapus","Informasi",
                    JOptionPane.INFORMATION_MESSAGE);
            }
        }

        }
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        if(txt_nip.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, NIP belum diisi!");
            txt_nip.requestFocus();
        }else if(txt_nama.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Nama belum diisi!");
            txt_nama.requestFocus();
        }else if(txt_user.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Username belum diisi!");
            txt_user.requestFocus();
        }else if(txt_pass.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Password belum diisi!");
            txt_pass.requestFocus();
        }else if(txt_confirm.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Confirm Password belum diisi!");
            txt_confirm.requestFocus();
        }else {
            String NIP = "", Nama = "", Username = "", Password = "", Confirm = "";
            try {
                
                dsn.setNama(txt_nama.getText());
                dsn.setUsername(txt_user.getText());
                dsn.setPass(txt_pass.getText());
                dsn.setConfirm(txt_confirm.getText());
                dsn.setNip(txt_nip.getText());

                dsn.ubahData(dsn.getNama(), dsn.getUsername(), dsn.getPass(), dsn.getConfirm(), dsn.getNip());

                JOptionPane.showMessageDialog(null, "Data berhasil diubah" , "Informasi",
                    JOptionPane.INFORMATION_MESSAGE);
                tampil_database();
                reset_text();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Data gagal diubah","Informasi",
                    JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_editActionPerformed

    private void tbl_dsnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_dsnMouseClicked
        try {
            int row = tbl_dsn.rowAtPoint(evt.getPoint());

            String NIP = tbl_dsn.getValueAt(row, 0).toString();
            String Nama = tbl_dsn.getValueAt(row, 1).toString();
            String Username = tbl_dsn.getValueAt(row, 2).toString();
            String Pass = tbl_dsn.getValueAt(row, 3).toString();
            String Confirm = tbl_dsn.getValueAt(row, 4).toString();

            txt_nip.setText(String.valueOf(NIP));
            txt_nama.setText(String.valueOf(Nama));
            txt_user.setText(String.valueOf(Username));
            txt_pass.setText(String.valueOf(Pass));
            txt_confirm.setText(String.valueOf(Confirm));
        } catch(Exception e){

        }
    }//GEN-LAST:event_tbl_dsnMouseClicked


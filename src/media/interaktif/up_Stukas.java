    public void tampil_database(){
        Object[]baris = {"Bab", "Kasus",};
        tabmode = new DefaultTableModel(null, baris);
        tbl_stukas.setModel(tabmode);
        try{
            hasil = stukas.tampilData();
            while(hasil.next()){
                stukas.setBab(hasil.getString("Bab"));
                stukas.setKasus(hasil.getString("Kasus"));
                String[] data = {stukas.getBab(), stukas.getKasus()};
                tabmode.addRow(data);
            } 
        } catch(Exception e){
        }
    }
    
